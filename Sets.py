a = {1, 3, 5, 7, 8, 10, 12}
print("A set is : ", a)
b = {2, 4, 6, 8, 10, 12}
print("B set is : ", b)
print("Difference between A set and B set (A-B) is = ", a.difference(b))
print("Symmetric difference between A set and B set  is = ", a.symmetric_difference(b))
print("Union of a set and b set (A ∪ B) is = ", a.union(b))
print("Intersection of a set and b set (A ∩ B) is = ", a.intersection(b))