num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
def compute_lcm(a, b):
    if a > b:
        greater = a
    else:
        greater = b

    while True:
        if greater % a == 0 and greater % b == 0:
            lcm = greater
            break
        greater += 1

    return lcm
lcm = compute_lcm(num1, num2)
print(f"The LCM of {num1} and {num2} is {lcm}")